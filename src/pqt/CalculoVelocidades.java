    private void btn_CalcularVcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_CalcularVcActionPerformed
        if (!txt_RPMd.getText().equals("") && !txt_Dmin.getText().equals("") && !txt_Dmax.getText().equals("")) {
            int Vc;
            Vc = (int) ((Double.parseDouble(txt_RPMd.getText()) * Math.PI * Double.parseDouble(txt_Dmin.getText())) / 1000.0);
            tbl_Vc.setValueAt(Vc, 0, 0);
            Vc = (int) ((Double.parseDouble(txt_RPMd.getText()) * Math.PI * Double.parseDouble(txt_Dmax.getText())) / 1000.0);
            tbl_Vc.setValueAt(Vc, 0, 1);
        } else {
            JOptionPane.showConfirmDialog(this, "Para el cálculo necesita Dmax, Dmin, y RPM deseada", "Faltan datos", JOptionPane.CLOSED_OPTION);
        }
    }//GEN-LAST:event_btn_CalcularVcActionPerformed

