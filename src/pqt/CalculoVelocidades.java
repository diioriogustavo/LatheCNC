    private void btn_CalcularRPMActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_CalcularRPMActionPerformed
        if (!txt_Dmax.getText().equals("") && !txt_Dmin.getText().equals("") && !txt_Vmax.getText().equals("") && !txt_Vmin.getText().equals("")) {
            int RPM;
            RPM = (int) ((1000.0 * Double.parseDouble(txt_Vmin.getText())) / (Math.PI * Double.parseDouble(txt_Dmin.getText())));
            tbl_RPM.setValueAt(RPM, 0, 0);
            RPM = (int) ((1000.0 * Double.parseDouble(txt_Vmin.getText())) / (Math.PI * Double.parseDouble(txt_Dmax.getText())));
            tbl_RPM.setValueAt(RPM, 0, 1);
            RPM = (int) ((1000.0 * Double.parseDouble(txt_Vmax.getText())) / (Math.PI * Double.parseDouble(txt_Dmin.getText())));
            tbl_RPM1.setValueAt(RPM, 0, 0);
            RPM = (int) ((1000.0 * Double.parseDouble(txt_Vmax.getText())) / (Math.PI * Double.parseDouble(txt_Dmax.getText())));
            tbl_RPM1.setValueAt(RPM, 0, 1);            
        } else {
            JOptionPane.showConfirmDialog(this, "Para el cálculo necesita Dmax, Dmin, Vcmax, y Vcmin", "Faltan datos", JOptionPane.CLOSED_OPTION);
        }
    }//GEN-LAST:event_btn_CalcularRPMActionPerformed

